export function calculateAgeInMonths(birthDate: string, measureDate: string): number {
  const birth = new Date(birthDate);
  const measure = new Date(measureDate);
  const months = (measure.getFullYear() - birth.getFullYear()) * 12 + (measure.getMonth() - birth.getMonth());
  const dayDiff = measure.getDate() - birth.getDate();
  return months + dayDiff / 30;
}

export function calculatePercentile(
  value: number,
  ageMonths: number,
  measureType: 'weight' | 'height' | 'headCircumference',
  gender: 'male' | 'female',
  standard: string = 'WHO'
): number | null {
  const data = getStandardData(standard);
  const genderKey = gender === 'male' ? 'boys' : 'girls';
  const percentileData = data[measureType][genderKey];

  const { months } = percentileData;
  
  // Find surrounding months for interpolation
  let lowerIdx = -1;
  for (let i = 0; i < months.length - 1; i++) {
    if (ageMonths >= months[i] && ageMonths <= months[i + 1]) {
      lowerIdx = i;
      break;
    }
  }
  if (lowerIdx === -1) return null;

  const t = (ageMonths - months[lowerIdx]) / (months[lowerIdx + 1] - months[lowerIdx]);

  const percentiles = [1, 3, 15, 50, 85, 97, 99];
  const keys: (keyof PercentileData)[] = ['P1', 'P3', 'P15', 'P50', 'P85', 'P97', 'P99'];

  // Interpolate percentile values at this age
  const interpValues: number[] = keys.map(key => {
    const arr = percentileData[key] as number[];
    return arr[lowerIdx] + t * (arr[lowerIdx + 1] - arr[lowerIdx]);
  });

  // Determine where value falls
  if (value <= interpValues[0]) return percentiles[0];
  if (value >= interpValues[interpValues.length - 1]) return percentiles[percentiles.length - 1];

  for (let i = 0; i < interpValues.length - 1; i++) {
    if (value >= interpValues[i] && value <= interpValues[i + 1]) {
      const ratio = (value - interpValues[i]) / (interpValues[i + 1] - interpValues[i]);
      return percentiles[i] + ratio * (percentiles[i + 1] - percentiles[i]);
    }
  }

  return null;
}

